      <h3><b>The Bible</b></h3>
      <p>We believe the Bible is the Word of God, inerrant and inspired in its original autographs, a complete and final revelation of God (2 Tim. 3:16; 2 Pet. 1:19; Rev. 22:18 – 19).</p>

      <h3><b>The Nature of God</b></h3>
      <p>We believe in one god, personal and knowable, who manifests Himself in three persons, the Father, the son and the Holy Spirit, co – eternal in being, co – identical in nature, co – equal in power and glory, and having the same attributes and perfection (Deut. 6:4; Matt. 28:19 – 20; Jn. 6:27; 2 Cor. 13:14; 1 Jn. 5:20).</p>

      <h3><b>The Deity of Jesus Christ</b></h3>
      <p>We believe that Jesus is God manifested in the flesh, born of a virgin, without sin and the chosen Messiah Who will one day set – up His kingdom on earth (Isa. 9:6 – 7; Matt. 1:23; Luke 1:35; Jn. 1:1; 14; 20:30 – 31; 1 Tim. 3:16; Heb. 4:15; Rev. 11:15; 19:11 – 16; 20:1 – 6).</p>

      <h3><b>The Salvation</b></h3>
      <p>We believe that all men are sinners and guilty before God, deserving judgment in needs of a Savior, Jesus Christ our Lord (Rom. 5:12 – 21; Eph. 2:1 – 2).</p>
      <p> We believe that Jesus Christ died on the cross for our sins, was buried, and rose again from the dead, ascended back to heaven, and one day will return to this earth (Jn. 14:1 – 3; Acts 1:9 – 11; 1 cor. 15:1 – 28, 50 – 58). <br><br>
      <p> We believe salvation from sin, death, and hell, is by God’s grace based on the finished work of Jesus Christ alone, and is received by faith apart from any human performance or merit (Jn. 14:6; Acts 16:31; Eph. 2:1 – 10).
      </p>

      <h3><b>The Baptism in the Holy Spirit</b></h3>
      <p>We believe in the Baptism of the Holy Spirit and fire is a gift from God as promised by the Lord Jesus Christ to all believers in the dispensation, and is received subsequent to the New Birth. This experience is accompanied by the initial evidence of speaking in other tongues as the Holy Spirit Himself gives utterance (Matt. 3:11; Acts 1:8; 2:38 – 39; 19:1 – 7; Mk. 16:16).</p>

      <h3><b>The Sanctification</b></h3>
      <p>We believe in the Doctrine of Sanctification as a definite yet progressive work of grace commencing at the time of regeneration and continuing until the consummation of salvation of Christ’s return (Heb. 12:14; 1 Thes. 5:23; 2 Pet. 3:18; 2 Cor. 3:18; 1 Cor. 1:30; Phil. 3:12 – 14).</p> <br>

      <h3><b>The Divine Healing and Deliverance</b></h3>
      <p>We believe that healing is for physical ills of human body and is wrought by the power of God through the prayer of faith, and by the laying on of hands. It is provided for in atonement of Christ, and is the privilege of every member of the Church today (Mk. 16:18; Jm. 5:14 – 15; 1 Pet. 2:24; Mt. 8:17; Isa. 53:4 – 5; Luke 4:18 – 19; Isa. 61:1a; Acts 10:38)</p> <br>

      <h3><b>The Church Ordinances</b></h3>
      <p>A. Water Baptism – We believe that Baptism by immersion in water is a public declaration of our identification with Christ and His death, burial, and resurrection (Matt. 28:19 – 20; Mk. 16:16; Acts 2:41, 8:36 – 39; 10:47 – 48; Rom. 6:3 – 4; Col. 2:12</p> <br>
      <p> B. The Lord’s Supper – We believe that this sacrament proclaims the death of Christ and bring to mind the fellowship meals of the risen Lord with His disciples (1 Cor. 11:21 – 32).
      </p>

      <h3><b>Tithes and Offerings</b></h3>
      <p>We believe that God has instructed every Christian to participate in advancing His Kingdom by giving their Tithes and Offerings (Mal. 3:10; 2 cor. 9:6 – 10).</p> <br>

      <h3><b>Final Judgment</b></h3>
      <p>We believe that each person will stand before God in judgment and receive either eternal reward or eternal judgment (Rev. 20:11 – 15; Matt. 25:31 - 46).</p> <br>

      <h3><b>Second Coming of Christ</b></h3>
      <p>We believe that Christ will return to this earth with power and visible to all and His return can be anytime (Matt. 24:30 – 31; 1 Thes. 4:16 – 17; 1 Thes. 5:1 – 2; 1 Cor. 15:51 - 53).</p> <br>

      <h3><b>Gifts and Fruit of the Holy Spirit</b></h3>
      <p>We believe that God gives spiritual gifts and that all the gifts in the New Testament are still available to the Church today (1 Cor. 12:1 – 11, 28; Eph. 4:7 – 11; Rom. 12:6 – 8).</p> <br>
      <p> We believe that the fruit of the Holy Spirit is being wrought by God Himself in the life of the believer and such fruit shall be evident in the believer’s life (Gal. 5:22 - 23).
      </p>

      <h3><b>The Great Commission</b></h3>
      <p>We believe in Evangelism and Missions, that winning individuals, families and nations to God through the good news of Christ is the principal purpose of the church (Prov. 11:30; Matt. 28:19 – 20; Mk.16:15; 2 Cor. 5:18; James 5:20).</p> <br>

